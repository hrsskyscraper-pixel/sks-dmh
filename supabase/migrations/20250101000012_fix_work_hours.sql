WHERE EXTRACT(DOW FROM d) NOT IN (0, 6)
  AND d::DATE NOT IN (
    '2025-04-29','2025-05-03','2025-05-04','2025-05-05','2025-05-06',
    '2025-07-21','2025-08-11','2025-09-15','2025-09-23'
