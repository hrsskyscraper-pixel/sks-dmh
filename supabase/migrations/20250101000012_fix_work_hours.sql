-- =============================================
-- 011_fix_work_hours.sql
-- テストデータの勤務時間を見直し
-- 各社員が「異なるフェーズにいる」状況を再現
-- =============================================
--
-- 社員マイルストーン（社員）:
--   4月フェーズ: 0〜500h
--   5〜6月フェーズ: 500〜900h
--   7〜8月フェーズ: 900〜1400h
--
-- 目標とする標準進捗と実績の対比:
--   山田 健二 : 勤務〜5月末  (~320h)  標準42%  実績36% ▼苦戦
--   鈴木 太郎 : 勤務〜7月末  (~648h)  標準74%  実績50% ▼遅れ
--   橋本さくら: 勤務〜7月末  (~640h)  標準74%  実績85% ▲先行  ※009で設定済み
--   田中 花子 : 勤務〜9月末  (~968h)  標準89%  実績76% ▼やや遅れ
--   松本 勇太 : 勤務〜9月末  (~960h)  標準89%  実績100% ▲大幅先行 ※009で設定済み
--   佐藤 美咲 : 勤務〜11月末 (~1280h) 標準97%  実績89% ▼わずか遅れ
-- =============================================

-- ─────────────────────────────────────────────
-- 山田 健二: 入社2ヶ月分（〜5月末）約320h
-- ─────────────────────────────────────────────
DELETE FROM work_hours WHERE employee_id = 'b1000000-0000-0000-0000-000000000004';
INSERT INTO work_hours (employee_id, work_date, hours)
SELECT 'b1000000-0000-0000-0000-000000000004'::UUID, d::DATE, 8.0
FROM generate_series('2025-04-01'::DATE, '2025-05-31'::DATE, '1 day') d
WHERE EXTRACT(DOW FROM d) NOT IN (0, 6)
  AND d::DATE NOT IN ('2025-04-29','2025-05-03','2025-05-04','2025-05-05','2025-05-06')
ON CONFLICT DO NOTHING;

-- ─────────────────────────────────────────────
-- 鈴木 太郎: 入社4ヶ月分（〜7月末）約648h
-- ─────────────────────────────────────────────
DELETE FROM work_hours WHERE employee_id = 'b1000000-0000-0000-0000-000000000002';
INSERT INTO work_hours (employee_id, work_date, hours)
SELECT 'b1000000-0000-0000-0000-000000000002'::UUID, d::DATE, 8.0
FROM generate_series('2025-04-01'::DATE, '2025-07-31'::DATE, '1 day') d
WHERE EXTRACT(DOW FROM d) NOT IN (0, 6)
  AND d::DATE NOT IN (
    '2025-04-29','2025-05-03','2025-05-04','2025-05-05','2025-05-06','2025-07-21'
  )
ON CONFLICT DO NOTHING;

